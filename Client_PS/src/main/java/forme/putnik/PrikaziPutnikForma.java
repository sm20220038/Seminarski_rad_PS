    private void ucitajPutnikeUTabelu() {
        List<Putnik> putnici = kontroler.vratiSvePutnike(putnik);
        putnici.sort(Comparator.comparingLong(Putnik::getId));
        if(putnici.isEmpty()){
            model = new ModelTabelePrikazPutnik(putnici);
            tblPutnici.setModel(model);
            JOptionPane.showMessageDialog(this, "Sistem ne moze da nadje putnika po zadatim kriterijumima", "Uspeh", JOptionPane.ERROR_MESSAGE);
            
        } else {
            model = new ModelTabelePrikazPutnik(putnici);
            tblPutnici.setModel(model);
            JOptionPane.showMessageDialog(this, "Sistem je nasao putnike po zadatim kriterijumima", "Uspeh", JOptionPane.INFORMATION_MESSAGE);
        }
    }
    
    private void initTabela(){
        List<Putnik> putnici = kontroler.ucitajSvePutnike();
        putnici.sort(Comparator.comparingLong(Putnik::getId));
        model = new ModelTabelePrikazPutnik(putnici);
        tblPutnici.setModel(model);
    }
