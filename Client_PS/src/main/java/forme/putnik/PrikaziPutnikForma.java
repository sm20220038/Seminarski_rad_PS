    private void btnPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziActionPerformed
        String ime = txtIme.getText().trim();
        String prezime = txtPrezime.getText().trim();
        String email = txtEmail.getText().trim();
        VrstaKategorije vrstaKategorija = (VrstaKategorije) cmbBoxKategorija.getSelectedItem();
        Kategorija kategorija = new Kategorija(-1, vrstaKategorija, null);
        kategorija.setId(kategorija.getIdByVrsta(vrstaKategorija));
        putnik = new Putnik((long) 0, ime, prezime, email, kategorija);
        ucitajPutnikeUTabelu();    
    }//GEN-LAST:event_btnPretraziActionPerformed

    private void btnPrikazPutnikaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrikazPutnikaActionPerformed
        int red = tblPutnici.getSelectedRow();
        if (red != -1) {
            Putnik putnik = model.getPutnici().get(red);
            JOptionPane.showMessageDialog(this, "Sistem je nasao putnika", "Uspeh", JOptionPane.INFORMATION_MESSAGE);
            
            KreirajPutnikForma forma = new KreirajPutnikForma();
            forma.setPutnikListener(this);
            forma.setPutnik(putnik);
            forma.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Sistem ne moze da nadje putnika", "Greska!", JOptionPane.ERROR_MESSAGE);
        }  
    }//GEN-LAST:event_btnPrikazPutnikaActionPerformed

